function soma(a,b){
    return a+b
}

function subtracao(a,b){
    return a-b
}

function divisao(a,b){
    return a / b
}

function multiplicacao(a,b){
    return a * b
}

module.exports.soma = soma
module.exports.subtracao = subtracao
module.exports.multiplicacao = multiplicacao
module.exports.divisao = divisao